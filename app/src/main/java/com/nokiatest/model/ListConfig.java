package com.nokiatest.model;


public class ListConfig {
    private String acsId;
    private String acsName;
    private String acsUrl;
    private Integer acsSyncFrecuency;

    public String getAcsId() {
        return acsId;
    }

    public void setAcsId(String acsId) {
        this.acsId = acsId;
    }

    public String getAcsName() {
        return acsName;
    }

    public void setAcsName(String acsName) {
        this.acsName = acsName;
    }

    public String getAcsUrl() {
        return acsUrl;
    }

    public void setAcsUrl(String acsUrl) {
        this.acsUrl = acsUrl;
    }

    public Integer getAcsSyncFrecuency() {
        return acsSyncFrecuency;
    }

    public void setAcsSyncFrecuency(Integer acsSyncFrecuency) {
        this.acsSyncFrecuency = acsSyncFrecuency;
    }


}
